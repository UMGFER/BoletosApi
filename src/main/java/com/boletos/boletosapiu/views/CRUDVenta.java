    public void cargarCRUDVenta(){
        SwingUtilities.invokeLater(() -> {
            setImageLabel(LabelLogo, "/ChatGPT Image 9 oct 2025, 03_07_12 p.m..png");
            setImageLabel(LabelLogo2, "/LOGO GUATE SELE.png");
        });
        customSpinnerDate();
        scrollModificar.getVerticalScrollBar().setUI(new CustomScrollBarUI());
        cargarListas();
        
        model = new DefaultTableModel(new String [] {"Nombre del Comprador", "Correo del Comprador","Tipo de Pago","Fecha de la Venta","Total de la Venta"}, 0);
        tblVentas.setModel(model);
        
        cargarVentas();
    }
    
    private void setImageLabel(javax.swing.JLabel label, String resourcePath) {
        java.net.URL imgURL = getClass().getResource(resourcePath);
        if (imgURL != null) {
            ImageIcon icon = new ImageIcon(imgURL);
            Image img = icon.getImage().getScaledInstance(
                    label.getWidth(),
                    label.getHeight(),
                    Image.SCALE_SMOOTH
            );
            label.setIcon(new ImageIcon(img));
        } else {
            System.err.println("❌ No se encontró la imagen: " + resourcePath);
        }
    }
    
    private void cargarListas(){       
        try {
            listaVentas = serviceVenta.getVentas();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar las ventas." + ex.getMessage(),"Error", 0);
        }
        
        try {
            listaDetalle = serviceDetalle.getDetalleVentas();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar los detalles de venta." + ex.getMessage(),"Error", 0);
        }
        
        sortListas();
    }
    
    private void sortListas(){
        listaVentas.sort((id1, id2) -> Integer.compare(id1.getId_venta(), id2.getId_venta()));
        listaDetalle.sort((id1, id2) -> Integer.compare(id1.getId_detalle(), id2.getId_detalle()));
    }
    
    private void cargarVentas(){
        model.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        for(Venta v : listaVentas){
            model.addRow(new Object[]{v.getNombre_comprador(), v.getCorreo_comprador(), v.getTipo_pago(), sdf.format(v.getFecha_venta()), v.getTotal_venta()});
        }
    }
    
    private void fillFieldsFromSelectedRow() {
        int row = tblVentas.getSelectedRow();
        txtNombreComprador.setText(listaVentas.get(row).getNombre_comprador());
        txtCorreoComprador.setText(listaVentas.get(row).getCorreo_comprador());
        cmbTipoPago.setSelectedItem(listaVentas.get(row).getTipo_pago());
        spnTotalVenta.setValue(listaVentas.get(row).getTotal_venta().doubleValue());
        
        spnCantidadBoletos.setValue(listaDetalle.get(row).getCantidad());
        spnPrecioU.setValue(listaDetalle.get(row).getPrecio_unitario().doubleValue());
        spnDescuento.setValue(listaDetalle.get(row).getDescuento().doubleValue());
    }
    
    private void customSpinnerDate(){
        spnPrimeraFecha.setModel(new SpinnerDateModel());
        JSpinner.DateEditor dateEditor = new JSpinner.DateEditor(spnPrimeraFecha, "dd-MM-yyyy");
        spnPrimeraFecha.setEditor(dateEditor);
        
        spnSegundaFecha.setModel(new SpinnerDateModel());
        JSpinner.DateEditor dateEditor2 = new JSpinner.DateEditor(spnSegundaFecha, "dd-MM-yyyy");
        spnSegundaFecha.setEditor(dateEditor2);
    }
    
    private void crearRegistros(Venta venta, DetalleVenta detalle){
        try {
            serviceVenta.updateVenta(venta.getId_venta(), venta);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar la venta." + ex.getMessage(), "Error", 0);
            return;
        }
        
        try{
            serviceDetalle.updateDetalleVenta(detalle.getId_detalle(), detalle);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al actualizar el detalle de venta." + ex.getMessage(), "Error", 0);
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Venta actualizada correctamente!");
    }
    
    private void clearFields(){
        txtNombreComprador.setText("");
        txtCorreoComprador.setText("");
        spnTotalVenta.setValue(0);
        spnCantidadBoletos.setValue(0);
        spnPrecioU.setValue(0);
        spnDescuento.setValue(0);
    }
