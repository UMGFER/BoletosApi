    private void btnFiltrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrosActionPerformed
        model.setRowCount(0);
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");  
        Date now = new Date();
        Date spn1 = (Date) spnPrimeraFecha.getValue();
        Date spn2 = (Date) spnSegundaFecha.getValue();

        boolean isSpn1Today = sdf.format(spn1).equals(sdf.format(now));
        boolean isSpn2Today = sdf.format(spn2).equals(sdf.format(now));
        int selectedType = cmbTipoPagoFiltro.getSelectedIndex();

        if ((!isSpn1Today || !isSpn2Today) && selectedType != 0) {
            for(Venta v : listaVentas){
                Date fechaVenta = v.getFecha_venta();
                String tipoPago = v.getTipo_pago();
                if(!fechaVenta.before(spn1) && !fechaVenta.after(spn2) && tipoPago.equals(cmbTipoPagoFiltro.getSelectedItem())){
                    model.addRow(new Object[]{v.getNombre_comprador(), v.getCorreo_comprador(), v.getTipo_pago(), sdf.format(v.getFecha_venta()), v.getTotal_venta()});
                }
                
            }
        } else if ((!isSpn1Today || !isSpn2Today) && selectedType == 0) {
            for(Venta v : listaVentas){
                Date fechaVenta = v.getFecha_venta();
                if(!fechaVenta.before(spn1) && !fechaVenta.after(spn2)){
                    model.addRow(new Object[]{v.getNombre_comprador(), v.getCorreo_comprador(), v.getTipo_pago(), sdf.format(v.getFecha_venta()), v.getTotal_venta()});
                }
            }
        } else if (isSpn1Today && isSpn2Today && selectedType != 0) {
            for(Venta v : listaVentas){
                String tipoPago = v.getTipo_pago();
                if(tipoPago.equals(cmbTipoPagoFiltro.getSelectedItem())){
                    model.addRow(new Object[]{v.getNombre_comprador(), v.getCorreo_comprador(), v.getTipo_pago(), sdf.format(v.getFecha_venta()), v.getTotal_venta()});
                }
            }
        }else if(isSpn1Today && isSpn2Today && selectedType == 0){
            for(Venta v : listaVentas){
                model.addRow(new Object[]{v.getNombre_comprador(), v.getCorreo_comprador(), v.getTipo_pago(), sdf.format(v.getFecha_venta()), v.getTotal_venta()});
            }
        }
        sortListas();
    }//GEN-LAST:event_btnFiltrosActionPerformed


