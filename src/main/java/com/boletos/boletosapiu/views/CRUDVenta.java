    private void btnActualizarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarVentaActionPerformed
        if(tblVentas.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar registro en la tabla.", "Error", 0);
            return;
        }
        
        int registro = tblVentas.getSelectedRow();
        
        Venta venta = new Venta();
        venta.setId_venta(listaVentas.get(registro).getId_venta());
        venta.setId_usuario(listaVentas.get(registro).getId_usuario());
        venta.setNombre_comprador(txtNombreComprador.getText());
        venta.setCorreo_comprador(txtCorreoComprador.getText());
        venta.setTipo_pago((String)cmbTipoPago.getSelectedItem());
        venta.setFecha_venta(listaVentas.get(registro).getFecha_venta());
        venta.setTotal_venta(BigDecimal.valueOf((double)spnTotalVenta.getValue()));
        
        DetalleVenta detalle = new DetalleVenta();
        detalle.setId_detalle(listaDetalle.get(registro).getId_detalle());
        detalle.setId_venta(listaDetalle.get(registro).getId_venta());
        detalle.setId_localidad(listaDetalle.get(registro).getId_localidad());
        detalle.setId_partido(listaDetalle.get(registro).getId_partido());
        detalle.setCantidad((int)spnCantidadBoletos.getValue());
        
        detalle.setPrecio_unitario(BigDecimal.valueOf((double)spnPrecioU.getValue()));
        detalle.setDescuento(BigDecimal.valueOf((double)spnDescuento.getValue()));
        
        crearRegistros(venta, detalle);
        
        cargarListas();      
        clearFields();
    }//GEN-LAST:event_btnActualizarVentaActionPerformed

